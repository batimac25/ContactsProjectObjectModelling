package com.contact;

public class GroupName {

	private String friends;
	private String family;
	private String coworkers;
	private Custom createNewGroup;
	public String getFriends() {
		return friends;
	}
	public void setFriends(String friends) {
		this.friends = friends;
	}
	public String getFamily() {
		return family;
	}
	public void setFamily(String family) {
		this.family = family;
	}
	public String getCoworkers() {
		return coworkers;
	}
	public void setCoworkers(String coworkers) {
		this.coworkers = coworkers;
	}
	public Custom getCreateNewGroup() {
		return createNewGroup;
	}
	public void setCreateNewGroup(Custom createNewGroup) {
		this.createNewGroup = createNewGroup;
	}
	
	
}
